import { Database } from '@nozbe/watermelondb'
import Deity from '../models/Deity'
import Stotra from '../models/Stotra'

export async function seedSaraswati(database: Database) {
    const saraswati = await database.get<Deity>('deities').create(deity => {
            deity.name = 'సరస్వతీ దేవి'
            deity.image = 'saraswati'
        })

        await database.get<Stotra>('stotras').create(stotra => {
            stotra.title = 'సరస్వతీ స్తోత్రం'
            stotra.content = `యా కుందేందు తుషారహారధవళా యా శుభ్రవస్త్రావృతా
యా వీణావరకుండమండితకరా యా శ్వేతపద్మాసనా |
యా బ్రహ్మాచ్యుత శంకరప్రభృతిభిర్దేవైః సదా పూజితా
సా మాం పాతు సరస్వతీ భగవతీ నిశ్శేషజాడ్యాపహా ||

శుక్లాం బ్రహ్మవిచార సార పరమామాద్యాం జగద్వ్యాపినీం
వీణాపుస్తక ధారిణీమభయదాం జాడ్యాంధకారాపహామ్ |
హస్తే స్ఫాటికమాలికాం విదధతీం పద్మాసనే సంస్థితాం
వందే తాం పరమేశ్వరీం భగవతీం బుద్ధిప్రదాం శారదామ్ ||

సరస్వతి నమస్తుభ్యం వరదే కామరూపిణి |
విద్యారంభం కరిష్యామి సిద్ధిర్భవతు మే సదా ||

జయ జయ దేవి చరాచర సారే
కుచయుగ శోభిత ముక్తాహారే |
వీణారంజిత పుస్తక హస్తే
భగవతి భారతి దేవి నమోస్తే ||`
            stotra.deity.set(saraswati)
            stotra.isFavorite = false
        })

        await database.get<Stotra>('stotras').create(stotra => {
            stotra.title = 'సరస్వతీ వందనా'
            stotra.content = `సరస్వతి నమస్తుభ్యం వరదే కామరూపిణి |
విద్యారంభం కరిష్యామి సిద్ధిర్భవతు మే సదా ||

యా దేవీ స్తూయతే నిత్యం విబుధైః వేదపారగైః |
సా మే వసతు జిహ్వాగ్రే బ్రహ్మరూపా సరస్వతీ ||

శుక్లాం బ్రహ్మవిచార సార పరమామాద్యాం జగద్వ్యాపినీం
వీణాపుస్తక ధారిణీమభయదాం జాడ్యాంధకారాపహామ్ |
హస్తే స్ఫాటికమాలికాం విదధతీం పద్మాసనే సంస్థితాం
వందే తాం పరమేశ్వరీం భగవతీం బుద్ధిప్రదాం శారదామ్ ||

యా కుందేందు తుషారహారధవళా యా శుభ్రవస్త్రావృతా
యా వీణావరకుండమండితకరా యా శ్వేతపద్మాసనా |
యా బ్రహ్మాచ్యుత శంకరప్రభృతిభిర్దేవైః సదా పూజితా
సా మాం పాతు సరస్వతీ భగవతీ నిశ్శేషజాడ్యాపహా ||`
            stotra.deity.set(saraswati)
            stotra.isFavorite = false
        })

        await database.get<Stotra>('stotras').create(stotra => {
            stotra.title = 'మేధా సూక్తం'
            stotra.content = `ఓం దేవీం వాచమజనయంత దేవాః
తాం విశ్వరూపాః పశవో వదంతి |
సా నో మంద్రేషమూర్జం దుహానా
ధేనుర్వాగస్మానుపసుష్టుతైతు ||

మహీ దేవీ మహిషాసురమర్దినీ
మహిషాసుర మర్దినీ సరస్వతీ |
వీణాపాణి వరదే దేవి
విద్యాదాయినీ నమోస్తుతే ||

సరస్వతీ మహాభాగే విద్యే కమలలోచనే |
విశ్వరూపే విశాలాక్షి విద్యాం దేహి నమోస్తుతే ||

జయ జయ దేవి చరాచర సారే
కుచయుగ శోభిత ముక్తాహారే |
వీణారంజిత పుస్తక హస్తే
భగవతి భారతి దేవి నమోస్తే ||`
            stotra.deity.set(saraswati)
            stotra.isFavorite = false
        })
}
